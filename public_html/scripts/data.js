function filterSpells(query) {
	const spellList = [];
	query = query.toLowerCase();
	for (const key in spells) {
		const spell = spells[key];
		if (spell.name && spell.name.toLowerCase().includes(query))
			spellList.push(spell);
	}
	return spellList;
}

